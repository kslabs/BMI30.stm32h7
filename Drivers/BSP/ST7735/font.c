/* Функция для получения индекса символа в массиве шрифта */
static inline int get_font_index(uint8_t char_code) {
    if (char_code >= 32 && char_code <= 126) {
        // ASCII символы: прямое сопоставление
        return char_code - 32;
    } else if (char_code >= 192 && char_code <= 255) {
        // Кириллические символы: смещение на 95
        return char_code - 192 + 95;
    }
    // Неподдерживаемый символ - возвращаем пробел
    return 0;
}

